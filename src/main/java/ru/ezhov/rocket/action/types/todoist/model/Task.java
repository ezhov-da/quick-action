package ru.ezhov.rocket.action.types.todoist.model;

public class Task {
    private String id;
    private String content;
    private String created;
    private String url;

    public String getId() {
        return id;
    }

    public String getContent() {
        return content;
    }

    public String getCreated() {
        return created;
    }

    public String getUrl() {
        return url;
    }
}
